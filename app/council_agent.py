def _extract_obj(text: str) -> dict | None:
    """Достаёт JSON-объект из ответа модели (HQ без robust-парсера)."""
    if not text:
        return None
    t = text.strip()
    if t.startswith("```"):
        t = t.strip("`")
        if t.lower().startswith("json"):
            t = t[4:]
    s, e = t.find("{"), t.rfind("}")
    if s >= 0 and e > s:
        for cand in (t[s:e + 1], re.sub(r",\s*([}\]])", r"\1", t[s:e + 1])):
            try:
                r = json.loads(cand)
                if isinstance(r, dict):
                    return r
            except Exception:
                continue
    return None


